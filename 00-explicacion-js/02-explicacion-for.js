// Imagina que tienes 5 juguetes en tu caja
let totalJuguetes = 5;

// Este es el bucle "for". Comienza con el número 1 y va hasta el número total de juguetes que tienes.
// Dentro del paréntesis, el primer número es donde comienza (en este caso, en 1), el segundo número es hasta dónde va (en este caso, hasta 5), y el tercer número es de cuánto en cuánto se incrementa (en este caso, de uno en uno).
for (let i = 1; i <= totalJuguetes; i++) {
    // Ahora, en cada vuelta del bucle, mostraremos el número del juguete que estamos contando.
    console.log("Juguete número " + i);
}

// Después de que el bucle haya terminado de contar los juguetes, mostrará "Juguete número 1", "Juguete número 2", "Juguete número 3", "Juguete número 4" y "Juguete número 5".
