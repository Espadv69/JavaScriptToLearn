// Esto es cómo creas una matriz vacía
let miArray = [];

// Y así es como puedes poner cosas dentro de esa matriz
miArray.push("manzana");
miArray.push("banana");
miArray.push("naranja");

// También puedes poner números
miArray.push(1);
miArray.push(2);
miArray.push(3);

// Incluso puedes poner otra matriz dentro de una matriz
miArray.push(["perro", "gato", "pájaro"]);

// Y para ver lo que tienes en tu matriz, puedes imprimirlo
console.log(miArray);