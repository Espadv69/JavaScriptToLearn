let contador = 1; // Empezamos desde el número 1

while (contador <= 10) { // Mientras el contador sea menor o igual a 10
  console.log(contador); // Imprime el número actual
  contador++; // Aumenta el contador en 1 para pasar al siguiente número
}


// Entonces, ¿cómo funciona esto?

// Iniciamos con un contador en 1.
// El "while" dice: "Mientras el contador sea igual o menor que 10, haz lo siguiente:".
// En cada paso, imprimimos el número actual y luego aumentamos el contador en 1.
// Esto se repite hasta que el contador llegue a 10. Cuando el contador llega a 11, la condición del "while" ya no se cumple, así que deja de ejecutarse.